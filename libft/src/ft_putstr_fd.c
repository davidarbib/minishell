#include <unistd.h>
#include "libft.h"

void	ft_putstr_fd(char *s, int fd)
{
	write(fd, s, ft_strlen(s));
}

int		putstr_fd(char *s, int fd)
{
	return ((int)write(fd, s, ft_strlen(s)));
}
